<template>
<div class="basic-detail">
  <a-form-model
    ref="ruleForm"
    :model="form"
    :rules="rules"
    :label-col="{span:6}"
    :wrapper-col="{span:13}"
  >
    <a-form-model-item label="商品售价" prop="price" required>
      <a-input
        v-model="form.price"
      />
    </a-form-model-item>
    <a-form-model-item label="商品折扣价" prop="price_off">
      <a-input
        v-model="form.price_off"
      />
    </a-form-model-item>
    <a-form-model-item label="商品库存" prop="inventory" required>
      <a-input
        v-model="form.inventory"
      />
    </a-form-model-item>
    <a-form-model-item label="计量单位" prop="unit" required>
        <a-input
        v-model="form.unit"
      />
    </a-form-model-item>
    <a-form-model-item label="商品相册" prop="images">
        <a-upload
      :action="'https://mallapi.duyiedu.com/upload/images?appkey=' + $store.state.user.appkey"
      list-type="picture-card"
      :file-list="fileList"
      @preview="handlePreview"
      @change="handleChange"
      name="avatar"
    >
      <div v-if="fileList.length < 8">
        <a-icon :type="loading?'loading':'plus'" />
        <div class="ant-upload-text">
          上传图片
        </div>
      </div>
    </a-upload>
    <a-modal :visible="previewVisible" :footer="null" @cancel="handleCancel">
      <img alt="example" style="width: 100%" :src="previewImage" />
    </a-modal>
    </a-form-model-item>
    <a-form-model-item class="next-btn" :wrapper-col="{span:24}">
        <a-button type="default" @click="prev">上一步</a-button>
        <a-button class="btn" type="primary" @click="next">下一步</a-button>
    </a-form-model-item>
  </a-form-model>
</div>
</template>

<script>
function getBase64(file) {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.readAsDataURL(file);
    reader.onload = () => resolve(reader.result);
    reader.onerror = (error) => reject(error);
  });
}
export default {
  data() {
    return {
      rules: {},
      fileList: [],
      loading: false,
      previewVisible: false,
      previewImage: '',
    };
  },
  props: ['form'],
  created() {
    if (this.form.images.length > 0) {
      this.fileList = this.form.images.map((item, index) => ({
        uid: index,
        name: `image-${index}.png`,
        status: 'done',
        url: item,
      }));
    }
  },
  methods: {
    handleCancel() {
      this.previewVisible = false;
    },
    next() {
      this.$emit('next', this.form);
      this.$refs.ruleForm.validate((valid) => {
        if (valid) {
          this.$emit('next', this.form);
          return true;
        }
        console.log('error submit!!');
        return false;
      });
    },
    prev() {
      this.$emit('prev');
    },
    async handlePreview(file) {
      const obj = file;
      if (!file.url && !file.preview) {
        obj.preview = await getBase64(file.originFileObj);
      }
      this.previewImage = file.url || file.preview;
      this.previewVisible = true;
    },
    handleChange({ file, fileList, event }) {
      console.log('change', file, fileList, event);
      if (file.status === 'done') {
        this.form.images.push(file.response.data.url);
      } else if (file.status === 'removed') {
        const { url } = file.response.data.url;
        this.form.images = this.form.images.filter((item) => item !== url);
      }
      this.fileList = fileList;
    },
  },
};
</script>

